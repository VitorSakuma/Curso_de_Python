frase = "Microsoft Power BI"

#Trocando uma palavra
trocaPalavra = frase.replace("Power BI" , "AZ-900")
print(trocaPalavra)

#Converter todas as letras para maiuscula "upper"
print(frase.upper())

#Converter todas as letras para minusculas "lower"
print(frase.lower())

#Converter primeira letra da frase "capitalize"
print(frase.capitalize())

#Converter primeira letra de cada palavra da frase "title"
print(frase.title())

cursoEspacado = "       Fundamentos da Ciência de Dados - Google Cloud                "

#Como remover espaços inadequados
print(cursoEspacado.strip())


#Como remover espaços inadequados da Direita "rstrip"
print(cursoEspacado.rstrip())

#Como remover espaços inadequados da Esquerda "lstrip"
print(cursoEspacado.lstrip())

